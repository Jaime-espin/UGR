    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarActionPerformed
        // TODO add your handling code here:
        String titulo = jTextField1.getText().trim();
        String descripcion = jTextField2.getText();
        String tipo = jComboBox1.getSelectedItem().toString();
        String prior = jComboBox2.getSelectedItem().toString();
        TipoTarea tarea;
        
        if(titulo.isEmpty()){
            return;
        }
        
        try{
            int n = Integer.parseInt(titulo);
            JOptionPane.showMessageDialog(this, "El título no puede ser solo un número.");
            return;    
        }catch(Exception e){
            
        }
        
        for (Tarea t : listaTarea) {
            if (t.getTitulo().equalsIgnoreCase(titulo)) { 
                JOptionPane.showMessageDialog(this, "Ya existe una tarea con ese título.","Error de Duplicado", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Prioridad prioridad = Prioridad.ALTA;
        switch (prior) {
            case "Alta":
                prioridad = Prioridad.ALTA;
                break;
            case "Media":
                prioridad = Prioridad.MEDIA;
                break;
            case "Baja":
                prioridad = Prioridad.BAJA;
                break;
            default:
                break;
        }
        Tarea nuevaTarea = null;
        switch (tipo) {
            case "Personal":
                tarea = TipoTarea.PERSONAL;
                nuevaTarea = new TareaPersonal(titulo, descripcion, prioridad);
                break;
            case "Estudio":
                tarea = TipoTarea.ESTUDIO;
                nuevaTarea = new TareaEstudio(titulo, descripcion, prioridad);
                break;
            case "Trabajo":
                tarea = TipoTarea.TRABAJO;
                nuevaTarea = new TareaTrabajo(titulo, descripcion, prioridad);
                break;
            default:
                break;
        }
        
        if (nuevaTarea != null) {
            listaTarea.add(nuevaTarea);
            this.listaDetalles.add(nuevaTarea.mostrarDetalles());

            // Actualizamos jList2
            jList2.setListData(listaDetalles.toArray(new String[0]));

            // Opcional: Limpiar los campos de entrada
            jTextField1.setText("");
            jTextField2.setText("");
        }
        
        
        
    }//GEN-LAST:event_BotonAgregarActionPerformed

