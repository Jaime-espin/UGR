    private void BotonElminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonElminarActionPerformed
        // TODO add your handling code here:
        int indice = jList2.getSelectedIndex();
        
        if(indice >= 0){
            listaTarea.remove(indice);
            
            listaDetalles.remove(indice);

            jList2.setListData(listaDetalles.toArray(new String[0]));
        }else{
            JOptionPane.showMessageDialog(this, "Debes seleccionar una tarea para eliminar.","Error de Selección", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_BotonElminarActionPerformed

